	<h1>Digital Talent Scholarship</h1>

	<p><h4>Program Digital Talent Scholarship Tahun 2019 (DTS 2019) merupakan program pengembangan sumber daya manusia (SDM) yang dilaksanakan Kementerian Komunikasi dan Informatika (Kemenkominfo) sebagai bagian dari program pembangunan prioritas nasional.</h4></p>

	<p><h4>Program pelatihan kompetensi DTS 2019 ditujukan untuk meningkatkan keterampilan (up-skilling) SDM bidang Komunikasi dan Informatika sehingga dapat meningkatkan produktivitas dan daya saing bangsa.</h4></p>
	<br>
	<br>

	<h2>Program DTS 2019 secara garis besar dibagi menjadi empat akademi, yaitu:</h2>


<p><h5>
	<li>Fresh Graduate Academy (FGA) , program pelatihan bagi lulusan D3, D4 dan S1 bidang TIK dan MIPA, terbuka bagi penyandang disabilitas;</li>

	<li>Vocational School Graduate Academy (VSGA) , program pelatihan bagi lulusan SMK;</li>

	<li>Coding Teacher Academy (CTA) , program pelatihan bagi para guru yang mengajar bidang TIK pada SMK, SMA, SMALB, dan Madrasah Aliyah (Terbuka bagi Guru PNS dan Non PNS);</li>
	<li>Online Academy (OA) , program pelatihan secara online bagi masyarakat umum termasuk ASN, mahasiswa, dan pelaku industri.</li>
</h5></p>
<br>
<br>
<p><h4>Program Vocational School Graduate Academy (VSGA) merupakan program pelatihan bidang TIK yang berfokus untuk meningkatkan kompetensi bagi 5.225 orang lulusan SMK yang belum bekerja. Program VSGA bertujuan untuk mencetak talenta/SDM level teknisi dalam bidang digital, khususnya dalam bidang Junior Network Administrator, Junior Graphic Designer, Junior Web Developer, Junior Mobile Programmer, dan Intermediate Animator.</h4></p>


